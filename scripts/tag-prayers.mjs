// 카테고리별 기본 태그
const categoryDefaults = {
  감사: ["보편"],
  치유: ["건강"],
  인도: ["보편"],
  가정: ["가족"],
  직장: ["직장인"],
  시험: ["보편"],
  회개: ["신앙", "보편"],
  평안: ["보편"],
  아침: ["보편"],
  저녁: ["보편"],
};

function tagPrayer(p) {
  const tags = new Set();
  // 카테고리 기본
  (categoryDefaults[p.category] || []).forEach((t) => tags.add(t));
  // 제목·본문에서 키워드 매칭
  const hay = `${p.title} ${p.content}`;
  for (const rule of rules) {
    if (rule.kw.some((k) => hay.includes(k))) {
      rule.tags.forEach((t) => tags.add(t));
    }
  }
  // 태그가 카테고리 기본만 있고 "보편"이 없으면 보편 추가 (검색 누락 방지)
  if (tags.size === 0) tags.add("보편");
  return [...tags];
}

const tagged = prayers.map((p) => ({ ...p, tags: tagPrayer(p) }));
writeFileSync(file, JSON.stringify(tagged, null, 2) + "\n");
console.log(`tagged ${tagged.length} prayers`);

// 통계
const tagCounts = {};
tagged.forEach((p) => p.tags.forEach((t) => { tagCounts[t] = (tagCounts[t] || 0) + 1; }));
